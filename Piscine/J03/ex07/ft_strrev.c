char	*ft_strrev(char *str)
{
	char	swap;
	int		i;
	int		j;

	i = 0;
	j = 0;
	while (str[i])
		i++;
	i = i - 1;
	while (i > j)
	{
		swap = str[j];
		str[j] = str[i];
		str[i] = swap;
		i--;
		j++;
	}
	return (str);
}
