void	ft_sort_integer_table(int *tab, int size)
{
	int		i;
	int		swap;

	i = 0;
	while (i < size)
	{
		if (tab[i + 1] <= tab[i])
		{
			swap = tab[i + 1];
			tab[i + 1] = tab[i];
			tab[i] = swap;
			i = 0;
		}
		else
			i++;
	}
}
